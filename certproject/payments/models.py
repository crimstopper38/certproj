from django.db import models

class Payments(models.Model):
    id = models.AutoField(primary_key=True)
    activity = models.CharField(max_length=100)
    first_name = models.CharField(max_length=100)
    last_name = models.CharField(max_length=100)
    employee_id = models.IntegerField()
    order_num = models.CharField(max_length=50)
    amt = models.IntegerField()
    cert_num = models.IntegerField(blank=True, null=True)
    mpp = models.IntegerField()
    bank = models.BooleanField()
    ese = models.DateField(auto_now=False, auto_now_add=False)
    needs_course = models.BooleanField()
    has_course = models.DateField(auto_now=False, auto_now_add=False)
    reading_comp = models.DateField(auto_now=False, auto_now_add=False, blank=True, null=True)
    needs_fels = models.BooleanField()
    has_fels = models.DateField(auto_now=False, auto_now_add=True)
    pending = models.CharField(max_length=50, blank=True, null=True)
    returned = models.DateField(auto_now=False, auto_now_add=False, blank=True, null=True)
    done = models.DateField(auto_now=False, auto_now_add=False, blank=True, null=True)
    alert = models.BooleanField()
    cert_notes = models.TextField()
    purchase_notes = models.TextField()